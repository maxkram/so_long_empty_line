#include "../../include/libft.h"

char	*ft_strdup(const char *s)
{
	char		*str;
	size_t		len;
	size_t		i;

	i = 0;
	len = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
